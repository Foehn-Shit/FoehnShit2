function bldSk(obj) {
    switch (obj) {
        case "CONTROL":
            return "控制中枢";
            break;
        case "POWER":
            return "发电站"
            break;
        case "MANUFACTURE":
            return "制造站"
            break;
        case "TRADING":
            return "贸易站"
            break;
        case "WORKSHOP":
            return "加工站"
            break;
        case "TRAINING":
            return "训练室"
            break;
        case "DORMITORY":
            return "宿舍"
            break;
        case "HIRE":
            return "办公室"
            break;
        case "MEETING":
            return "会客室"
            break;
    }
}