function bldSk(obj) {
    switch (obj) {
        case "CONTROL":
            return "控制中枢";
        case "POWER":
            return "发电站";
        case "MANUFACTURE":
            return "制造站";
        case "TRADING":
            return "贸易站";
        case "WORKSHOP":
            return "加工站";
        case "TRAINING":
            return "训练室";
        case "DORMITORY":
            return "宿舍";
        case "HIRE":
            return "办公室";
        case "MEETING":
            return "会客室";
    }
}